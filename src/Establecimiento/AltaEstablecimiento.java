            String sql = "INSERT INTO establecimiento (nombre,ubicacion,pcod)"+
            " VALUES ('"+nombre+"', '"+ubicacion+"', '"+pcod+"');";

            //String sql = "INSERT INTO prod ( pcod,dni,nombre,telefono,domicilio) "
